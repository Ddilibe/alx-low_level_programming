#include "main.h"
#include <stdio.h>
#include <string.h>
/**
 * puts2 - print every other character of a string 
 * starting with every first character
 * @str : input string
 * Return: no return
 */

void puts2(char *str)
{
	int l, p, m;

	l = strlen(str);
	for (p = 0; p < l; p++)
	{
		if ((p == 0) || (p % 2 == 0))
			printf("%c", str[p]);
	}
	printf("\n");
}
