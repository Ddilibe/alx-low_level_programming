#include <stdio.h>

/**
 * add - Function to add two variables
 * @a: variable A
 * @b: variable B
 *
 * Return: (a+b)
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - Function to subtract two variables
 * @a: variable A
 * @b: variable B
 *
 * Return: (a-b)
 */

int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - Function to multiply two variables
 * @a: variable A
 * @b: variable B
 *
 * Return: (a*b)
 */

int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - Function to divide two variables
 * @a: variable A
 * @b: variable B
 *
 * Return: (a/b)
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - Function to remainder two variables
 * @a: variable A
 * @b: variable B
 *
 * Return: (a%b)
 */

int mod(int a, int b)
{
	return (a % b);
}
